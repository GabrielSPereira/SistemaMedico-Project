    private void jButton1ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton1ActionPerformed
        Paciente p = new Paciente();
        p.setNome(txNome.getText());
        p.setEndereco(txEndereco.getText());
        p.setCpf(txCpf.getText());
        p.setIdade(Integer.parseInt(txIdade.getText()));
        p.setTelefone(txTelefone.getText());
        p.setRg(txRg.getText());
        medicosTela.get(posTela).ListaPacientes.add(p);
        Entrar_Medico em = new Entrar_Medico(medicosTela, posTela);
        em.setVisible(true);
        dispose();
    }//GEN-LAST:event_jButton1ActionPerformed

