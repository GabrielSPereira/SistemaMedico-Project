    private void jButton4ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton4ActionPerformed
        Ocorrencias e = new Ocorrencias();
        e.setNumero(Integer.parseInt(txNumero.getText()));
        e.setDescricao(txDescricao.getText());
        e.setData(txData.getText());
        e.setData(txData.getText());
        Date d = new Date();
        SimpleDateFormat sdf = new SimpleDateFormat("dd/MM/yyyy"); 
        e.setDataCadastro(sdf.format(d));
        Medico m = medicosTela.get(posMedTela);
        Paciente p = m.ListaPacientes.get(posPacTela);
        e.setIsParticular(true);
        if(jRadioButton1.isSelected()){
            e.setIsParticular(false);
        }            
        p.ListaOcorrencias.add(e);
        Prontuario em = new Prontuario(medicosTela, posPacTela, posMedTela);
        em.setVisible(true);
        dispose();
    }//GEN-LAST:event_jButton4ActionPerformed

